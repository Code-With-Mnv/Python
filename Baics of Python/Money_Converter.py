# Conversion rate from Rupees to Dollars
conversion_rate = 0.012  # Example rate, 1 Rupee = 0.012 Dollars

# Prompt the user to enter the amount in Rupees
rupees = float(input("Enter the amount in Rupees: "))

# Calculate the amount in Dollars
dollars = rupees * conversion_rate

# Print the result
print(f"{rupees} Rupees is equal to {dollars} Dollars")